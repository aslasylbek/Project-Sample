struct EnglishModel: Codable {
    let success: Int?
    let message: String?
    let info: [Info]?
    let english: [English]?
}

struct English: Codable {
    let courseID, dispName, teacherID, teacherFio: String?
    let vsk1, vsk2, exam: JSONAny?
    let total: String?
    let topics: [Topic]?
    
    enum CodingKeys: String, CodingKey {
        case courseID = "course_id"
        case dispName = "disp_name"
        case teacherID = "teacher_id"
        case teacherFio = "teacher_fio"
        case vsk1, vsk2, exam, total, topics
    }
}

struct Topic: Codable {
    let topicID: String?
    let number: JSONAny?
    let title: String?
    let endRead, startListen, endListen, startGram, startVoc, endVoc, startRead: JSONAny?
    let endGram: JSONAny?
    let words: [Word]?
    let listening: [Listening]?
    let reading: [ReadingModel]?
    let grammar: [GrammarModel]?
    
    enum CodingKeys: String, CodingKey {
        case topicID = "topic_id"
        case number, title
        case startVoc = "start_voc"
        case endVoc = "end_voc"
        case startRead = "start_read"
        case endRead = "end_read"
        case startListen = "start_listen"
        case endListen = "end_listen"
        case startGram = "start_gram"
        case endGram = "end_gram"
        case words, listening, reading, grammar
    }
}

struct Grammar: Codable {
    let id, sentence, question, answer: String?
    let translate: String?
    let soundURL: String?
    
    enum CodingKeys: String, CodingKey {
        case id, sentence, question, answer, translate
        case soundURL = "sound_url"
    }
}

struct Listening: Codable {
    let id, listening: String?
    let translate: String?
    let questionanswer: [Questionanswer]?
    let soundURL: String?
    
    enum CodingKeys: String, CodingKey {
        case id, listening, translate, questionanswer
        case soundURL = "sound_url"
    }
}


struct Reading: Codable {
    let id, reading: String?
    let questionanswer: [Questionanswer]?
    let truefalse: [Truefalse]?
}

struct Word: Codable {
    let id, word: String?
    let type: String?
    let picURL: String?
    let transcription: String?
    let soundURL: String?
    let translateWord: String?
    
    enum CodingKeys: String, CodingKey {
        case id, word, type
        case picURL = "pic_url"
        case transcription
        case soundURL = "sound_url"
        case translateWord = "translate_word"
    }
}

struct Info: Codable {
    let fio, barcode, group, code: String?
    let program, course: String?
}

